import { serve } from "https://deno.land/std@0.190.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2.55.0";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

interface GooglePlaceResult {
  place_id: string;
  name: string;
  vicinity?: string;
  formatted_address?: string;
  rating?: number;
  price_level?: number;
  types: string[];
  geometry: {
    location: {
      lat: number;
      lng: number;
    };
  };
  photos?: Array<{
    photo_reference: string;
    height: number;
    width: number;
  }>;
  opening_hours?: {
    open_now?: boolean;
  };
}

interface ImportRequest {
  location: string;
  radius: number;
  maxResults: number;
  testMode: boolean;
}

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    console.log("üöÄ D√©but de l'importation de restaurants depuis Google Places");

    // V√©rification des secrets
    const googleMapsApiKey = Deno.env.get("GOOGLE_MAPS_API_KEY");
    const supabaseUrl = Deno.env.get("SUPABASE_URL");
    const supabaseServiceKey = Deno.env.get("SUPABASE_SERVICE_ROLE_KEY");

    console.log("üîë V√©rification des secrets:");
    console.log(`- Google Maps API Key: ${googleMapsApiKey ? 'PR√âSENT ‚úÖ' : 'MANQUANT ‚ùå'}`);
    console.log(`- Supabase URL: ${supabaseUrl ? 'PR√âSENT ‚úÖ' : 'MANQUANT ‚ùå'}`);
    console.log(`- Supabase Service Key: ${supabaseServiceKey ? 'PR√âSENT ‚úÖ' : 'MANQUANT ‚ùå'}`);

    if (!googleMapsApiKey) {
      console.error("‚ùå GOOGLE_MAPS_API_KEY manquant");
      throw new Error("GOOGLE_MAPS_API_KEY n'est pas configur√©");
    }

    if (!supabaseUrl || !supabaseServiceKey) {
      console.error("‚ùå Configuration Supabase manquante");
      throw new Error("Configuration Supabase manquante"); 
    }

    // Parse de la requ√™te
    let requestBody;
    try {
      requestBody = await req.json();
      console.log("üì¶ Body de la requ√™te re√ßu:", JSON.stringify(requestBody, null, 2));
    } catch (parseError) {
      console.error("‚ùå Erreur parsing JSON:", parseError);
      throw new Error("Corps de requ√™te JSON invalide");
    }

    const { location, radius, maxResults, testMode } = requestBody;

    console.log(`üìç Recherche de restaurants pr√®s de: ${location}`);
    console.log(`üìä Param√®tres: rayon=${radius}km, max=${maxResults}, test=${testMode}`);

    // Initialisation du client Supabase avec les permissions admin
    const supabase = createClient(supabaseUrl, supabaseServiceKey);
    console.log("‚úÖ Client Supabase initialis√©");

    // Coordonn√©es fixes pour Montr√©al et Repentigny
    let lat: number, lng: number;
    
    console.log(`üó∫Ô∏è S√©lection des coordonn√©es pour: ${location}`);
    
    if (location.toLowerCase().includes('montreal') || location.toLowerCase().includes('montr√©al')) {
      lat = 45.5017;  // Montr√©al centre-ville
      lng = -73.5673;
      console.log(`üìç Coordonn√©es Montr√©al: ${lat}, ${lng}`);
    } else if (location.toLowerCase().includes('repentigny')) {
      lat = 45.7420;  // Repentigny centre-ville
      lng = -73.4500;
      console.log(`üìç Coordonn√©es Repentigny: ${lat}, ${lng}`);
    } else {
      // Par d√©faut Montr√©al si autre ville
      lat = 45.5017;
      lng = -73.5673;
      console.log(`üìç Par d√©faut Montr√©al pour: ${location}`);
    }
    console.log(`üó∫Ô∏è Coordonn√©es trouv√©es: ${lat}, ${lng}`);

    // Convertir le rayon de km en m√®tres pour l'API Google
    const radiusInMeters = radius * 1000;
    console.log(`üìè Rayon: ${radius}km (${radiusInMeters}m)`);

    // Recherche de restaurants via Places API (restaurants et casse-cro√ªtes uniquement)
    const placesUrl = `https://maps.googleapis.com/maps/api/place/nearbysearch/json?location=${lat},${lng}&radius=${radiusInMeters}&type=restaurant&key=${googleMapsApiKey}`;
    
    console.log(`üîç URL Places API: ${placesUrl.replace(googleMapsApiKey, '***API_KEY***')}`);
    
    const placesResponse = await fetch(placesUrl);
    const placesData = await placesResponse.json();

    console.log(`üì° R√©ponse Places API:`, JSON.stringify(placesData, null, 2));
    console.log(`üìä Status Places API: ${placesData.status}`);

    if (placesData.status !== "OK") {
      if (placesData.status === "REQUEST_DENIED" && placesData.error_message?.includes("referer restrictions")) {
        throw new Error(`PROBL√àME DE CONFIGURATION: Votre cl√© API Google a des restrictions de r√©f√©rent qui l'emp√™chent de fonctionner depuis un serveur. Vous devez soit:
1. Cr√©er une nouvelle cl√© API sans restrictions de r√©f√©rent pour les fonctions serveur
2. Ou modifier les restrictions de votre cl√© actuelle dans Google Cloud Console`);
      }
      throw new Error(`Erreur Places API: ${placesData.status} - ${placesData.error_message || 'Erreur inconnue'}`);
    }

    const restaurants = placesData.results as GooglePlaceResult[];
    const limitedRestaurants = restaurants.slice(0, maxResults);
    
    console.log(`üè™ ${limitedRestaurants.length} restaurants trouv√©s`);

    // Obtenir l'utilisateur authentifi√© (g√©r√© automatiquement par Supabase avec verify_jwt = true)
    console.log("üîê R√©cup√©ration de l'utilisateur authentifi√©...");
    
    let user_id: string;
    try {
      const authHeader = req.headers.get("Authorization");
      console.log(`üîë Auth header: ${authHeader ? 'PR√âSENT' : 'MANQUANT'}`);
      
      if (!authHeader) {
        throw new Error("En-t√™te Authorization manquant");
      }

      const token = authHeader.replace('Bearer ', '');
      const { data: { user }, error: authError } = await supabase.auth.getUser(token);
      
      console.log(`üë§ Utilisateur r√©cup√©r√©: ${user ? user.id : 'AUCUN'}`);
      
      if (authError) {
        console.error("‚ùå Erreur auth:", authError.message);
        throw new Error(`Erreur d'authentification: ${authError.message}`);
      }
      
      if (!user) {
        throw new Error("Utilisateur non authentifi√©");
      }

      user_id = user.id;
      console.log(`üë§ Import par l'utilisateur: ${user_id}`);
    } catch (authErr) {
      console.error("‚ùå Erreur d'authentification:", authErr);
      throw new Error(`Authentification √©chou√©e: ${authErr.message}`);
    }

    // Traitement et insertion des restaurants
    let successCount = 0;
    let errorCount = 0;
    const logs: string[] = [];

    for (const place of limitedRestaurants) {
      try {
        // Validation et transformation des donn√©es avec s√©curit√© renforc√©e
        if (!place.name || typeof place.name !== 'string') {
          console.log(`‚ö†Ô∏è Restaurant ignor√©: nom invalide`, place);
          logs.push(`‚ö†Ô∏è Restaurant ignor√©: nom invalide`);
          continue;
        }
        
        const restaurantName = place.name.trim();
        const address = (place.vicinity || place.formatted_address || 'Adresse non disponible').trim();
        
        // D√©terminer le type de cuisine bas√© sur les types Google
        const cuisineTypes = extractCuisineTypes(place.types);
        
        // D√©terminer la gamme de prix
        const priceRange = getPriceRange(place.price_level);

        console.log(`üîç V√©rification doublons pour: ${restaurantName}`);
        
        // V√©rification de doublons simplifi√©e et s√©curis√©e
        let isDuplicate = false;
        try {
          const { data: existingRestaurants, error: searchError } = await supabase
            .from('restaurants')
            .select('id, name, address')
            .eq('name', restaurantName)
            .limit(1);

          if (searchError) {
            console.error(`‚ö†Ô∏è Erreur recherche doublons pour ${restaurantName}:`, searchError);
            // En cas d'erreur de recherche, on continue l'insertion
            isDuplicate = false;
          } else {
            isDuplicate = existingRestaurants && existingRestaurants.length > 0;
            console.log(`üîç Doublons trouv√©s: ${isDuplicate ? 'OUI' : 'NON'} (${existingRestaurants?.length || 0} r√©sultats)`);
          }
        } catch (searchException) {
          console.error(`üí• Exception recherche doublons ${restaurantName}:`, searchException);
          isDuplicate = false; // En cas d'exception, on continue
        }

        if (isDuplicate) {
          logs.push(`‚ö†Ô∏è Restaurant "${restaurantName}" d√©j√† existant, ignor√©`);
          console.log(`‚ö†Ô∏è Restaurant "${restaurantName}" d√©j√† dans la DB, ignor√© pour √©viter doublon`);
          continue;
        }

        // Pr√©parer les donn√©es pour l'insertion avec validation renforc√©e
        const restaurantData = {
          name: restaurantName || 'Restaurant import√©',
          description: `Restaurant import√© depuis Google Places. ${address}`,
          description_fr: `Restaurant import√© depuis Google Places situ√© √† ${address}`,
          description_en: `Restaurant imported from Google Places located at ${address}`,
          address: address || null,
          phone: null, // N√©cessiterait Place Details API
          email: null, // Pas disponible via Places API
          cuisine_type: Array.isArray(cuisineTypes) ? cuisineTypes : ['Restaurant'],
          price_range: typeof priceRange === 'string' ? priceRange : 'moderate',
          owner_id: user_id, // Assign√© √† l'administrateur qui fait l'import
          is_active: true,
          dietary_restrictions: [],
          allergens: [],
          restaurant_specialties: [],
          service_types: ['dine_in'], // Par d√©faut
          delivery_radius: 5 // Par d√©faut
        };

        console.log(`üìù Donn√©es √† ins√©rer pour ${restaurantName}:`, JSON.stringify(restaurantData, null, 2));

        if (testMode) {
          logs.push(`üß™ [TEST] Restaurant serait ins√©r√©: ${restaurantName}`);
          successCount++;
        } else {
          // Insertion en base de donn√©es avec gestion d'erreur d√©taill√©e
          try {
            console.log(`üíæ Tentative d'insertion pour: ${restaurantName}`);
            
            const { data: insertedRestaurant, error: insertError } = await supabase
              .from('restaurants')
              .insert(restaurantData)
              .select('id, name')
              .single();

            if (insertError) {
              console.error(`‚ùå Erreur insertion ${restaurantName}:`, {
                message: insertError.message,
                details: insertError.details,
                hint: insertError.hint,
                code: insertError.code
              });
              logs.push(`‚ùå Erreur DB: ${restaurantName} - ${insertError.message} (code: ${insertError.code})`);
              errorCount++;
            } else {
              console.log(`‚úÖ Restaurant ins√©r√© avec succ√®s: ${restaurantName} (ID: ${insertedRestaurant.id})`);
              logs.push(`‚úÖ Succ√®s: ${restaurantName} import√© avec ID ${insertedRestaurant.id}`);
              successCount++;
            }
          } catch (dbError) {
            console.error(`üí• Exception lors de l'insertion ${restaurantName}:`, dbError);
            logs.push(`üí• Exception DB: ${restaurantName} - ${dbError.message}`);
            errorCount++;
          }
        }

      } catch (error) {
        console.error(`üí• Erreur traitement restaurant ${place.name}:`, {
          message: error.message,
          stack: error.stack,
          place_data: {
            name: place.name,
            place_id: place.place_id,
            types: place.types,
            rating: place.rating,
            price_level: place.price_level
          }
        });
        logs.push(`üí• Erreur traitement "${place.name}": ${error.message}`);
        errorCount++;
      }
    }

    const result = {
      success: successCount,
      errors: errorCount,
      total: limitedRestaurants.length,
      logs: logs,
      testMode: testMode
    };

    console.log("üìä R√©sultats de l'importation:", result);

    return new Response(JSON.stringify(result), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });

  } catch (error) {
    console.error("üí• Erreur g√©n√©rale:", {
      message: error.message,
      stack: error.stack,
      name: error.name
    });
    
    return new Response(
      JSON.stringify({ 
        error: error.message,
        errorType: error.name,
        success: 0,
        errors: 1,
        total: 0,
        logs: [`üí• Erreur g√©n√©rale: ${error.message}`],
        debug: {
          timestamp: new Date().toISOString(),
          stack: error.stack
        }
      }),
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});

// Fonction utilitaire pour extraire les types de cuisine (restaurants et casse-cro√ªtes uniquement)
function extractCuisineTypes(types: string[]): string[] {
  const cuisineMapping: Record<string, string> = {
    'restaurant': 'Restaurant',
    'meal_delivery': 'Restaurant avec livraison',
    'meal_takeaway': 'Restaurant √† emporter',
    'pizza': 'Pizzeria',
    'fast_food': 'Casse-cro√ªte',
    'food': 'Restaurant'
  };

  // Filtrer pour ne garder que les restaurants et casse-cro√ªtes
  const validTypes = ['restaurant', 'meal_delivery', 'meal_takeaway', 'pizza', 'fast_food', 'food'];
  const restaurantTypes = types.filter(type => validTypes.includes(type));
  
  const cuisines = restaurantTypes
    .map(type => cuisineMapping[type])
    .filter(Boolean);

  return cuisines.length > 0 ? cuisines : ['Restaurant'];
}

// Fonction utilitaire pour d√©terminer la gamme de prix
function getPriceRange(priceLevel?: number): string {
  switch (priceLevel) {
    case 0:
    case 1:
      return 'budget';
    case 2:
      return 'moderate';
    case 3:
      return 'expensive';
    case 4:
      return 'very_expensive';
    default:
      return 'moderate'; // Par d√©faut
  }
}